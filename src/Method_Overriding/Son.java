package Method_Overriding;

public class Son extends Father
{
	//Sub or Child class
	
	public void Bike()
	{
		System.out.println("Bike: FZ");
		
	}
	
	public void car()
	{
		System.out.println("Car: Kia");
	}
	
	public void Money()
	{
		System.out.println("Money: 2L");
	}

}
